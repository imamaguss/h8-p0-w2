console.log('\n2. Menyusun Barisan Bintang dengan Nested Looping\n');

var rows = 5;
var cols = 5;
var patch = '';

for(rows; rows > 0; rows--){
  for(cols; cols > 0; cols--){
    patch += '*';
  }
  console.log(patch);
}

